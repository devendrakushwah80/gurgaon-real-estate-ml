@st.cache_data(show_spinner=False)
def load_model_metrics() -> dict[str, float]:
    """Load model metrics generated by training."""

    return _read_json(DEFAULT_METRICS_PATH)


@st.cache_data(show_spinner=False)
def load_model_metadata() -> dict[str, Any]:
    """Load model metadata generated by training."""

    return _read_json(DEFAULT_MODEL_METADATA_PATH)


@st.cache_data(show_spinner=False)
def load_feature_importance() -> pd.DataFrame:
    """Load saved feature importance table."""

    if not DEFAULT_FEATURE_IMPORTANCE_PATH.exists():
        return pd.DataFrame(columns=["feature", "importance"])
    return pd.read_csv(DEFAULT_FEATURE_IMPORTANCE_PATH)


def model_artifact_status() -> dict[str, Any]:
    """Return local model artifact status for operational visibility."""

    artifact = MODEL_DIR / "price_model.joblib"
    return {
        "exists": artifact.exists(),
        "path": str(artifact),
        "size_mb": round(artifact.stat().st_size / (1024 * 1024), 2) if artifact.exists() else 0.0,
    }


def recent_predictions() -> pd.DataFrame:
    """Return recent predictions captured during the current Streamlit session."""

    records = st.session_state.get("recent_predictions", [])
    return pd.DataFrame(records)


def append_recent_prediction(record: dict[str, Any]) -> None:
    """Store a recent prediction in session state."""

    records = list(st.session_state.get("recent_predictions", []))
    records.insert(0, record)
    st.session_state["recent_predictions"] = records[:10]


def _read_json(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    return json.loads(path.read_text(encoding="utf-8"))


def _parse_coordinates(value: object) -> tuple[float | None, float | None]:
    text = str(value)
    try:
        lat_part, lon_part = text.split(",")
        lat = float(lat_part.replace("° N", "").strip())
        lon = float(lon_part.replace("° E", "").strip())
        return lat, lon
    except (ValueError, AttributeError):
        return None, None
