"""Real analytics data access for the frontend."""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

import pandas as pd
import streamlit as st

from src.analytics.insights import amenity_summary, locality_price_trends, property_type_summary
from src.config.paths import (
    DATA_DIR,
    DEFAULT_FEATURE_IMPORTANCE_PATH,
    DEFAULT_METRICS_PATH,
    DEFAULT_MODEL_METADATA_PATH,
    DEFAULT_RECOMMENDER_DATA,
    DEFAULT_TRAINING_DATA,
    MODEL_DIR,
)


@st.cache_data(show_spinner=False)
def load_market_data() -> pd.DataFrame:
    """Load the real processed modeling dataset."""

    return pd.read_csv(DEFAULT_TRAINING_DATA)


@st.cache_data(show_spinner=False)
def load_apartment_data() -> pd.DataFrame:
    """Load project metadata used by the recommender."""

    if not DEFAULT_RECOMMENDER_DATA.exists():
        return pd.DataFrame()
    return pd.read_csv(DEFAULT_RECOMMENDER_DATA)


@st.cache_data(show_spinner=False)
def load_geo_market_data() -> pd.DataFrame:
    """Load sector-level pricing data with parsed Gurgaon coordinates."""

    latlong_path = DATA_DIR / "external" / "latlong.csv"
    if not latlong_path.exists():
        return pd.DataFrame()

    market = load_market_data().copy()
    coords = pd.read_csv(latlong_path)
    coords[["lat", "lon"]] = coords["coordinates"].apply(_parse_coordinates).apply(pd.Series)
    grouped = (
        market.groupby("sector", dropna=False)
        .agg(
            median_price=("price", "median"),
            median_area=("built_up_area", "median"),
            listing_count=("price", "size"),
        )
        .reset_index()
    )
    grouped["price_per_sqft"] = (grouped["median_price"] * 10_000_000) / grouped["median_area"]
    merged = grouped.merge(coords[["sector", "lat", "lon"]], on="sector", how="inner")
    return merged.dropna(subset=["lat", "lon"])


